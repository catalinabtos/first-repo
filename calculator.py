# ADD
def add(a, b):
  return a + b

# SUBSTRACT
def substract(a, b):
  return a - b

# MULTIPLY
def multiply(a, b):
  return a * b

# DIVIDE
def divide(a, b):
  return a / b

# EXPONENT
def exp(a, b):
  return a ** b

print(add(5, 6))
print(substract(7, 6))
print(multiply(5,5))
print(divide (10,2))
print(exp(5,2))